package com.example.noteswithfriends.MVPTrial;

/**
Interface used for updating the view, which is part of the GUI.
 */
public interface IView {
    /**
    Used to set an object's string to parameter s. Note this method is generally used with widgets.
     @param s: the string that set within the object, generally a widget.
     */
    public void setString(String s);
}
